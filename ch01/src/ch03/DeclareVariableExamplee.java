package ch03;

public class DeclareVariableExamplee {
  public static void main(String[] args) {
	  int x=1; //연산실행문 - 변수선언 및 값 초기화, int x; x=1;
	  int y=2; //연산실행문 - 변수 선언 및 값 초기화, int y; y=2;
	  int result=x+y;//연산실행문 - 변수 선언 및 값 초기화 - 변수 x와 y에 저장된 값 연산할 결과를 대입
	  // int result; result=x+y;
	  System.out.println(result);//함수호출문-출력문
	  
  }//메소드 블럭
}//클래스블럭
