package ch07;

public class TwoDimension {
	public static void main(String[] args) {
		//이차원 배열
		//타입[][] 변수 = new 타입[행의갯수][열의갯수];
		int[][] scores = new int[2][3];
		//
		scores[0][0]=1;//[첫행][첫열]  [1][2][3]
		scores[0][1]=2;//[첫행][두열]
		scores[0][2]=3;//[첫행][삼열]
		
		scores[1][0]=4;//[두행][첫열]  [4][5][6]
		scores[1][1]=5;//[두행][두열]
		scores[1][2]=6;//[두행][삼열]
		
		
	
}
}
